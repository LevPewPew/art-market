  def index
    @q = User.all.ransack(params[:q])
    @users = @q.result(distinct: true).page(params[:page]).per(10)
  end

  def destroy_other
    @user.destroy
    respond_to do |format|
      format.html { redirect_to users_url, notice: 'User was successfully destroyed.' }
      format.json { head :no_content }
    end
  end

